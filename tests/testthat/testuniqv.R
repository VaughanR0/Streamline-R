context('Makeuniqv a logical vector')
library(hts2)

n1 <- list(3)
n2 <- list(2,c(3,1))
n3 <- list(4,c(2,1,1,1),c(1,2,1,2,1))
n4 <- list(4,c(1,1,1,2),c(2,1,1,1,1))
n5 <- list(4,c(1,1,1,2),c(1,1,1,2,1))
n6 <- list(6,c(2,1,1,1,1,1),c(1,1,1,1,1,2,1))

# count of nodes for each case, including Total
expect_equal(do.call(sum,n1) + 1,4)
expect_equal(do.call(sum,n2) + 1,7)
expect_equal(do.call(sum,n3) + 1,17)
expect_equal(do.call(sum,n4) + 1,16)
expect_equal(do.call(sum,n5) + 1,16)
expect_equal(do.call(sum,n6) + 1,22)

u1 <- makeuniqv(n1)
expect_equal(length(u1),4)
expect_true(is.logical(u1))
expect_true(all(u1),T)

u2 <- makeuniqv(n2)
expect_equal(length(u2),7)
expect_true(is.logical(u2))
expect_false(all(u2),T)

expect_equal(all(u2[1:6]),T)
expect_equal(u2[7],F)

u3 <- makeuniqv(n3)
expect_equal(length(u3),17)
expect_true(is.logical(u3))
expect_false(all(u3),T)

expect_equal(all(u3[1:7]),T)
expect_equal(all(u3[8:10]),F)
expect_equal(all(u3[11]),F)
expect_equal(all(u3[12:13]),T)
expect_equal(all(u3[14]),F)
expect_equal(all(u3[15:16]),T)
expect_equal(all(u3[17]),F)

u4 <- makeuniqv(n4)
#print(c("u4: ", paste(u4)))
expect_equal(length(u4),16)
expect_true(is.logical(u4))
expect_false(all(u4),T)

expect_equal(all(u4[1:5]),T)
expect_equal(all(u4[6:8]),F)
expect_equal(all(u4[9:12]),T)
expect_equal(all(u4[13:16]),F)

u5 <- makeuniqv(n5)
#print(c("u5: ", paste(u5)))
expect_equal(length(u5),16)
expect_true(is.logical(u5))
expect_false(all(u5),T)

expect_equal(all(u5[1:5]),T)
expect_equal(all(u5[6:8]),F)
expect_equal(all(u5[9:10]),T)
expect_equal(all(u5[11:13]),F)
expect_equal(all(u5[14:15]),T)
expect_equal(all(u5[16]),F)

u6 <- makeuniqv(n6)
#print(c("u6: ", paste(u6)))
expect_equal(length(u6),22)
expect_true(is.logical(u6))
expect_false(all(u6),T)

expect_equal(all(u6[1:9]),T)
expect_equal(all(u6[10:19]),F)
expect_equal(all(u6[20:21]),T)
expect_equal(all(u6[22]),F)
